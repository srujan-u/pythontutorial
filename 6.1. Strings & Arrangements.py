#Printing letter with positing from a string
dialogue3 = 'Letters are in the order'
print(dialogue3[0])
# 0 refers to the first letter of the string
#-1 refers to last letter of the string, -2 referes to the previous of the last letter of the string and similarly others

#Printing multiple letters from the string in order
print(dialogue3[0:7])

#this will print the whole string as there is no mentioned upper limit. It'll print the string from the position mentioned in the lower limit
print(dialogue3[0:])

#printing from Second letter to Last letter in the string
print(dialogue3[1:-1])
